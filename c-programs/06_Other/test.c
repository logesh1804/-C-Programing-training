#include <stdio.h>
int main() {

int age = 25; // Integer literal
float pi = 3.1415; // Floating-point literal
char grade = 'A'; // Character literal
char name[] = "Silica VLSI"; // String literal
int octal = 010; // Octal literal (8 in decimal)
int hex = 0xFF; // Hexadecimal literal (255 in decimal)
const int max = 100; // Constant literal

printf("Age: %d\n", age);
printf("PI: %.4f\n", pi);
printf("Grade: %c\n", grade);
printf("Name: %s\n", name);
printf("Octal (010): %d\n", octal);
printf("Hexadecimal (0xFF): %d\n", hex);
printf("Max Marks (const): %d\n", max);
return 0;
}




